    private void jPanel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel9MouseClicked
        // TODO add your handling code here:
        Player.incrementCount();
        p1= Player.getp1();
        p2=Player.getp2();
        if(p1==true)
        {
            symbol='X';
            Player.markBoard(symbol);
            Player.markPlayerA(2,0);
            jLabelG.setText(Character.toString(symbol));
        }
        else if(p2==true)
        {
            symbol='O';
            Player.markBoard(symbol);
            Player.markPlayerB(2,0);
            jLabelG.setText(Character.toString(symbol));
        }
        win=Player.calculateWin();
        if(win=="Player1 Wins" || win=="Player2 Wins" || win=="Game Draws")
        {
            JOptionPane.showMessageDialog(null, win);
        }
        else
        {
            turn=Player.calculatePlayerTurn();
            JOptionPane.showMessageDialog(null, turn);
        }
    }//GEN-LAST:event_jPanel9MouseClicked

